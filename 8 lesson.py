# Задача (Декораторы)
#
# Напиши декоратор repeat_twice, который будет вызывать декорируемую функцию два раза подряд.
# Подсказка: декоратор — это функция, которая принимает другую функцию и возвращает новую (обёртку). Внутри обёртки нужно вызвать исходную функцию дважды.
# После определения декоратора:
# Примени его к функции say_hello, которая просто печатает "Привет!"
# Вызови декорированную функцию

def repeat_twice(func):
    def wrapper():
        func()
        func()
        return
    return wrapper

@repeat_twice
def say_hello():
    print("Привет!")


# Задача (Декораторы)
#
# Напиши декоратор add_exclamation, который добавляет восклицательный знак в конец результата декорируемой функции.
# Подсказка: декорируемая функция будет возвращать строку. В функции-обёртке нужно вызвать исходную функцию, получить её результат, добавить к нему "!" и вернуть новую строку.
# После определения декоратора:
# Примени его к функции get_greeting, которая принимает имя name и возвращает строку "Привет, {name}"
# Вызови get_greeting с именем "Максим" и выведи результат

def add_exclamation(func):
    def wrapper(name):
        result = func(name)
        return result + "!"
    return wrapper

@add_exclamation
def get_greeting(name):
    return f"Привет, {name}"

print(get_greeting("Максим"))

